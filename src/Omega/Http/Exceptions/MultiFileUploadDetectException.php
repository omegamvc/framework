declare(strict_types=1);

namespace Omega\Http\Exceptions;

use InvalidArgumentException;

/**
 * Exception thrown when multiple files are detected but single file instance is expected.
 *
 * @category   Omega
 * @package    Http
 * @subpackage Exceptions
 * @link       https://omegamvc.github.io
 * @author     Adriano Giovannini <agisoftt@gmail.com>
 * @copyright  Copyright (c) 2025 Adriano Giovannini (https://omegamvc.github.io)
 * @license    https://www.gnu.org/licenses/gpl-3.0-standalone.html     GPL V3.0+
 * @version    2.0.0
 */
class MultiFileUploadDetectException extends InvalidArgumentException
{
    /**
     * Create a new MultiFileUploadDetectException instance.
     *
     * @return void
     */
    public function __construct()
    {
        parent::__construct('Single files detected use `UploadMultiFile` instances of `UploadFile`');
    }
}
